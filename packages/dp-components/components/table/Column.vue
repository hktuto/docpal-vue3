<script lang="ts" setup>
import dayjs from 'dayjs'
import ValueFilters from '../../stores/index'
const props = defineProps<{ col: Table.Column }>()
const emit = defineEmits(['command'])
// 按钮组事件
const handleAction = (command: Table.Command, { row, $index }: { row: any; $index: number }) => {
    emit('command', command, row, $index)
}
function getProp(row, prop?) {
    if(!prop) prop = props.col.prop
    let result = ''
    let nextValue = { ...row }
    prop.split('.').forEach(key => {
        result = nextValue[key]
        nextValue = nextValue[key]
    })
    return result
}
function formatProp (row) {
    console.log( props.col);
    if(!props.col.formatList || props.col.formatList.length === 0) return getProp(row)
    else {
        let result = ''
        props.col.formatList.forEach(item => {
            console.log(item);
            if (item.formatFun) {
                result += ValueFilters[item.formatFun](getProp(row, item.prop),deepCopy(item.params) )
            } else {
                result += getProp(row, item.prop)
            }
        })
        return result
    }
}
</script>
<template>
    <!-- 如果有配置多级表头的数据，则递归该组件 -->
    <template v-if="col.children?.length">
        <el-table-column :label="col.label" :width="col.width" :align="col.align">
            <TableColumn v-for="(item, index) in col.children" :col="item" :key="index" />
        </el-table-column>
    </template>
    <!-- 其他正常列 -->
    <el-table-column
        v-else v-bind="col">
        <template #default="{ row, $index }">
            <!---图片 (START)-->
            <!-- 如需更改图片size，可自行配置参数 -->
            <el-image
                v-if="col.type === 'image'"
                preview-teleported
                :hide-on-click-modal="true"
                :preview-src-list="[row[col.prop!]]"
                :src="getProp(row)"
                fit="cover"
                class="w-9 h-9 rounded-lg" />
            <!---图片 (END)-->
            <template v-else-if="col.type === 'date'">
                <!---十位数时间戳-->
                <span v-if="String(getProp(row))?.length <= 10">
                    {{ dayjs.unix(getProp(row)).format(col.dateFormat ?? 'YYYY-MM-DD') }}
                </span>
                <!---十三位数时间戳-->
                <span v-else>{{ dayjs(getProp(row)).format(col.dateFormat ?? 'YYYY-MM-DD') }}</span>
            </template>
            <!-- 如果传递按钮数组，就展示按钮组 START-->
            <el-button-group v-else-if="col.buttons?.length">
                <el-button
                    v-for="(btn, index) in col.buttons"
                    :key="index"
                    :size="btn.size"
                    :type="btn.type"
                    @click="handleAction(btn.command, { row, $index })"
                    >{{ btn.name }}</el-button
                >
            </el-button-group>
            <!-- 如果传递按钮数组，就展示按钮组 END-->
            <!-- render函数 (START) -->
            <!-- 使用内置的component组件可以支持h函数渲染和txs语法 -->
            <component v-else-if="col.render" :is="col.render" :row="row" :index="$index" />
            <!-- render函数 (END) -->
            <!-- 自定义slot (START) -->
            <slot v-else-if="col.slot" :slotName="col.slot" :row="row" :index="$index"></slot>
            <!-- 自定义slot (END) -->
            <!-- 默认渲染 (START) -->
            <span v-else>{{ formatProp(row) }}</span>
            <!-- 默认渲染 (END) -->
        </template>
        <!-- 自定义表头 -->
        <template #header="{ column, $index }">
            <component v-if="col.headerRender" :is="col.headerRender" :column="column" :index="$index" />
            <slot
                v-else-if="col.headerSlot"
                name="customHeader"
                :slotName="col.headerSlot"
                :column="column"
                :index="$index"></slot>
            <span v-else>{{ $t(column.label) }}</span>
        </template>
    </el-table-column>
</template>
